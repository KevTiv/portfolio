import type { NextPage } from 'next'
import Head from 'next/head'
import styles from '../styles/Styles.module.scss'

import Navbar from '../components/navbar/index'

import Intro from '../components/hero/intro'
import Portfolio from '../components/hero/portfolio'
import About from '../components/hero/about'
import Contact from '../components/hero/contact'
import Skills from '../components/hero/skills'
import { useEffect, useRef, useState } from 'react'
import { cursorAnimation, screenReveal } from '../animation/globalAnimation'
import Transition from '../components/hero/transition'
import { scrollToSection } from '../animation/navbarAnimation'



const Home: NextPage = () => {
  let pageContainerRef = useRef<HTMLDivElement>(null);
  
  
  useEffect(() => {
    //Use of gsap to smooth reload blink screen.
    // scrollToSection('#intro');
    screenReveal();

    const updateCursorPosition = (posX: number, posY: number)=>{
      // console.log('x: ' + posX + ' y: ' + posY);
      cursorAnimation(posX, posY);
    }

    pageContainerRef.current!.addEventListener('mousemove',e=>{
      // console.log(e);
      updateCursorPosition(e.clientX, e.clientY);
    });
    return()=>{
      pageContainerRef.current!.removeEventListener('mousemove',e=>{
        updateCursorPosition(e.clientX, e.clientY);
      });
    }
  },[])

  return (
    <div className={styles.container} ref={pageContainerRef}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Transition/>
      <Navbar />
      <main className={styles.main}>
        <span id='innerCursor' className={styles.inner_cursor}></span>
        <section  id="intro">
          <Intro/>
        </section>
        <section>
          <About/>
        </section>
        <section>
          <Skills />
        </section>
        <section>
          <Portfolio/>
        </section>
        <section>
          <Contact/>
        </section>
      </main>
    </div>
  )
}

export default Home
